package src;

public class Customer {
 
	private Waiter MuttonString, Chicken;
	
	public Customer(Waiter MuttonString, Waiter Chicken) {
		this.MuttonString = MuttonString;
		this.Chicken = Chicken;
	 
	}
	 
	public void muttonstring() {
		MuttonString.takeorder();
	 
	}
	 
	public void chicken() {
		Chicken.takeorder();
	 
	}
	 
}
 

	
	

